package org.pokemon.tcg.model;

import java.util.List;

public class TypeApi {

	private List<Type> types;

	public TypeApi(List<Type> types) {
		super();
		this.types = types;
	}

	public List<Type> getTypes() {
		return types;
	}

	public void setTypes(List<Type> types) {
		this.types = types;
	}
	
	
	
	
}
